//
public class Exam0160 {
  
  public static void main(String[] args) {
    //모든 변수를 다 초기화시킬 필요는 없다.
    int a1, a2 = 200, a3, a4 = 400, a5; /* a1 과 a2에 200을 할당하는 게 아니라 a1은 그냥 선언만 한것이고,
                                           a2에만 200을 할당한 것이다. a3 와 a4도 마찬가지이다. */
  }
}
